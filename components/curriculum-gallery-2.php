<!-- Initialize Swiper -->
<script>
    document.addEventListener('DOMContentLoaded', function() {
        var swiperBeyond = new Swiper(".beyond", {
            autoplay: {
                delay: 3000,
                disableOnInteraction: false,
            },
            loop: true,
            pagination: {
                el: ".swiper-pagination",
                dynamicBullets: true,
            },
            breakpoints: {
                600: {
                    slidesPerView: 1,
                    spaceBetween: 5,
                },
                768: {
                    slidesPerView: 3,
                    spaceBetween: 10,
                },
                998: {
                    slidesPerView: 3,
                    spaceBetween: 20,
                },
            },
        });

        // Pause autoplay on Beyond swiper container hover
        var swiperContainerBeyond = document.querySelector('.beyond');
        swiperContainerBeyond.addEventListener('mouseenter', function() {
            swiperBeyond.autoplay.stop();
        });

        swiperContainerBeyond.addEventListener('mouseleave', function() {
            swiperBeyond.autoplay.start();
        });
    });
</script>